package Main.SalesProject.Hello;

public class TeamDetails {
	
	int id;
	String Name;
	public TeamDetails() {
		
	}
	
	public TeamDetails(int id, String Name) {
		super();
		this.id = id;
		this.Name = Name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String Name) {
		this.Name = Name;
	}
	
	

}
